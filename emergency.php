    document.addEventListener("DOMContentLoaded", () => {
      const provinceSelect = document.getElementById("province");
      const districtSelect = document.getElementById("district");
      const localLevelSelect = document.getElementById("local-level");
      const ambulanceList = document.getElementById("ambulance-list");

      // Update districts when province changes
      provinceSelect.addEventListener("change", () => {
        const provinceId = provinceSelect.value;
        districtSelect.innerHTML = '<option value="0">सबै</option>';
        localLevelSelect.innerHTML = '<option value="0">सबै</option>';

        if (provinceId !== "0" && nepalData[provinceId]) {
          Object.keys(nepalData[provinceId].districts).forEach(district => {
            const option = document.createElement("option");
            option.value = district;
            option.textContent = district;
            districtSelect.appendChild(option);
          });
        }
        updateAmbulanceTable();
      });

      // Update local levels when district changes
      districtSelect.addEventListener("change", () => {
        const provinceId = provinceSelect.value;
        const districtName = districtSelect.value;
        localLevelSelect.innerHTML = '<option value="0">सबै</option>';

        if (provinceId !== "0" && districtName !== "0" && nepalData[provinceId].districts[districtName]) {
          nepalData[provinceId].districts[districtName].forEach(localLevel => {
            const option = document.createElement("option");
            option.value = localLevel;
            option.textContent = localLevel;
            localLevelSelect.appendChild(option);
          });
        }
        updateAmbulanceTable();
      });

      // Update table when local level changes
      localLevelSelect.addEventListener("change", updateAmbulanceTable);

      // Function to update ambulance table based on filter selections
      function updateAmbulanceTable() {
        const provinceId = provinceSelect.value;
        const province = provinceId === "0" ? "सबै" : nepalData[provinceId].name;
        const district = districtSelect.value === "0" ? "सबै" : districtSelect.value;
        const localLevel = localLevelSelect.value === "0" ? "सबै" : localLevelSelect.value;

        // Filter ambulance data
        const filteredAmbulances = ambulanceData.filter(ambulance => {
          const provinceMatch = province === "सबै" || ambulance.province === province;
          const districtMatch = district === "सबै" || ambulance.district === district;
          const localLevelMatch = localLevel === "सबै" || ambulance.localLevel === localLevel;
          return provinceMatch && districtMatch && localLevelMatch;
        });

        // Update table
        ambulanceList.innerHTML = "";
        if (filteredAmbulances.length === 0) {
          ambulanceList.innerHTML = '<tr><td colspan="4">कुनै एम्बुलेन्स सेवा उपलब्ध छैन।</td></tr>';
        } else {
          filteredAmbulances.forEach(ambulance => {
            const row = document.createElement("tr");
            row.innerHTML = `
              <td>${ambulance.province}</td>
              <td>${ambulance.district}</td>
              <td>${ambulance.hospital}</td>
              <td><a href="tel:${ambulance.phone}">${ambulance.phone}</a></td>
            `;
            ambulanceList.appendChild(row);
          });
        }
      }

      // Initial table population
      updateAmbulanceTable();
    });
